package eightsquarepackage;

public class OddNumbers {
    public static void main(String[] args) {
        for (int i = 1; i <101; i++) {
            if (i%2!=0) {
                System.out.println("Odd number =" + i);
            }
            else{
                if(i%2!=0){
                    System.out.println("Even number =" +i);
                }
            }
        }
    }
}
