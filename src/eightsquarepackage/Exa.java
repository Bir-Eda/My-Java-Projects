package eightsquarepackage;

public class Exa {
    public static void main(String[] args) {

        for (int i =1; i <6; i++) {
            System.out.print(i+"\t");
            for (int j = 1; j <=i; j++) {
                System.out.print(j+"\t");
            }
            System.out.println();
        }
    }
}
