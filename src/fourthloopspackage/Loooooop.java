package fourthloopspackage;

public class Loooooop {
    public static void main(String[] args) {
        for (int i = 0; i < 2; i += 2) {
            for (int j = 0; j < 2; j += 2) {
                for (int k = 0; k < 2; k += 2) {
                    System.out.println("" + i + j + k);
                }
            }
        }
    }
}
