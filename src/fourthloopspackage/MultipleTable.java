package fourthloopspackage;

public class MultipleTable{
    public static void main(String[] args) {
        for(int i=1; i<=5; i++){
            System.out.print(i+"\t");

        }
        System.out.println();

        for(int j=6; j<=10; j++){
            System.out.print(j + "\t");
        }
        System.out.println();
        for(int k=11; k<=15; k++){
            System.out.print(k+"\t");
        }

    }

}
