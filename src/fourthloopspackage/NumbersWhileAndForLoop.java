package fourthloopspackage;

public class NumbersWhileAndForLoop {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.print("\t" + i);
        }
        System.out.println();

        for (int j = 6; j <= 10; j++) {
            System.out.print("\t" + j);
        }
        System.out.println();

        for (int k = 11; k <= 15; k++) {
            System.out.print("\t" + k);
        }

    }
}
