package sixtharraypackage;

public class ArraySumNum {
    public static void main(String[] args) {
        int sum=0;
        //int counter=0;
        int[]numbers={1,2,3,4,5,6,7,8};
        for(int i=0; i<numbers.length; i++){
            sum=sum+numbers[i];
            //counter++;
        }
        System.out.println(sum);
        //System.out.println(counter);
    }
}
